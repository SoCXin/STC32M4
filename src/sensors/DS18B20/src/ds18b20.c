/**
 * DS18B20 CRC 校验函数
 * 多项式: X8+X5+X4+1
 */
uint8_t ds18b20_crc_calculate(uint8_t *data, uint8_t length)
{
  uint8_t i;
  uint8_t crc = 0;
  while(length--)
  {
    crc ^= *data++;
    for(i = 0;i < 8;i++)
    {
      if(crc & 0x01)
        // crc = (crc >> 1) ^ 0x8C;
        crc = crc8_table[crc >> 1];
      else
        crc >>= 1;
    }
  }
  return crc;
}

uint8_t ds18b20_init(void)
{
  // 首先释放总线
  DS18B20_PIN = 1;
  ds18b20_delay_10us();
  // 拉低480us ~ 960us
  DS18B20_PIN = 0;
  ds18b20_delay_500us();
  // 拉低后释放
  DS18B20_PIN = 1;
  // 等待15～60us
  ds18b20_delay_100us();
  if(DS18B20_PIN)
  {
    // 高电平状态无从机响应
    return 1;
  }
  ds18b20_delay_100us();
  return 0;
}

uint8_t ds18b20_read_byte()
{
  uint8_t i;
  uint8_t data = 0;
  for(i=0; i<8; i++)
  {
    DS18B20_PIN = 1;
    __nop(); __nop();
    DS18B20_PIN = 0;
    data >>= 1;
    DS18B20_PIN = 1;
    if(DS18B20_PIN)
    {
      data |= 0x80;
    }
    ds18b20_delay_50us();
  }
  return data;
}

void ds18b20_write_byte(uint8_t data)
{
  uint8_t i;
  for(i=0; i<8; i++)
  {
    DS18B20_PIN = 0;
    DS18B20_PIN = data & 0x01;
    ds18b20_delay_50us();
    DS18B20_PIN = 1;
    data >>= 1;
  }
  ds18b20_delay_50us();
}

uint8_t ds18b20_measuring(float *temperature, uint8_t resolution)
{
  uint8_t th, tl;
  uint8_t status;
  uint16_t temp;
  const uint8_t config = (resolution << 5) | 0x1F;

  status = ds18b20_init();
  if(status)
  {
    return 1;
  }
  ds18b20_write_byte(WRITE_SCRATCHPAD);
  ds18b20_write_byte(0x00);
  ds18b20_write_byte(0x00);
  ds18b20_write_byte(config);

  status = ds18b20_init();
  if(status)
  {
    return 2;
  }
  ds18b20_write_byte(SKIP_ROM);
  ds18b20_write_byte(CONVERT_T);
  delay_ms(MAX_CONVERSION_TIMES[resolution]);

  status = ds18b20_init();
  if(status)
  {
    return 3;
  }
  ds18b20_write_byte(SKIP_ROM);
  ds18b20_write_byte(READ_SCRATCHPAD);
  tl = ds18b20_read_byte();
  th = ds18b20_read_byte();

  if(th & 0xF0)
  {
    temp = (uint16_t)(~(((((uint16_t)th) << 8) | tl) - 1));
    *temperature = -((temp >> 4) + (((temp & 0x0F) >> (3 - resolution)) * DS18B20_RESOLUTIONS[resolution]));
  }
  else
  {
    *temperature = (((th & 0x0F) << 4) | (tl>> 4)) + (((tl & 0x0F) >> (3 - resolution)) * DS18B20_RESOLUTIONS[resolution]);
  }
  return status;
}

uint8_t ds18b20_read_rom(DS18B20_ROM *rom)
{
  uint8_t i;
  uint8_t status;
  uint8_t rom_bytes[8];
  status = ds18b20_init();
  if(status)
  {
    return 1;
  }
  ds18b20_write_byte(READ_ROM);
  for(i=0; i<8; i++)
  {
    *(rom_bytes + i) = ds18b20_read_byte();
  }
  if(ds18b20_crc_calculate(rom_bytes, 8))
  {
    return 2;
  }
  rom -> crc = *(rom_bytes + 7);
  rom -> family_code = *rom_bytes;
  // 数组声明后会在内存空间中开辟一段空间，而数组名指向那段内存空间的首地址，
  // 它相当于一个常量指针，所存放的地址不变，就像我们不能改变常量的值一样。
  // 因此数组名不能作为赋值表达式的左值。
  memcpy(rom -> serial_number, rom_bytes+1, 6);
  return 0;
}
